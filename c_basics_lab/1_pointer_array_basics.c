/*
============================================================
 C 기초 1단계: 변수 / 주소 / 포인터 / 배열  (+ 파이썬 비교)
============================================================

[파이썬이랑 가장 큰 차이]
파이썬에서는 "메모리"나 "주소"를 거의 신경 안 썼지?
    x = 5
    arr = [1, 2, 3]
이렇게 하면 파이썬이 알아서 메모리를 관리해줬어.
C는 그걸 다 '직접' 다뤄야 해. 그래서 이런 개념이 새로 나와:

------------------------------------------------------------
1) 변수 = "값을 담는 상자"
   - C:      int x = 5;   // x라는 상자에 '값 5' 자체를 담음
   - Python: x = 5        // 비슷해 보이지만, 파이썬 x는 사실 '5라는 객체를 가리키는 이름표'에 가까움
   => C는 상자에 값을 '직접' 넣는다고 생각하면 편함.

2) 주소(&) = "그 상자가 메모리 어디에 있는지"
   - C에는 &x 라는 게 있음 -> "x 상자의 위치(주소)"
   - 파이썬엔 이걸 직접 다루는 문법이 없음 (id(x)가 그나마 비슷)

3) 포인터(*) = "주소를 담는 상자"
   - int* p = &x;   // p는 '값 5'가 아니라 'x의 위치(주소)'를 담음
   - *p             // p가 가리키는 곳의 값 (= 5). 이걸 '역참조'라고 함
   - 파이썬엔 이런 명시적 포인터가 없음 (그래서 처음에 제일 낯설어)

4) 배열 = "같은 타입 상자 여러 개가 '연속으로' 붙어있는 것"
   - C:      int arr[3] = {1, 2, 3};
   - Python: arr = [1, 2, 3]
   - 핵심 차이:
       * 파이썬 list: 크기 자동으로 늘어남, 타입 섞기 OK, len(arr)로 길이 앎
       * C 배열   : 크기 고정, 타입 하나만, '길이 정보를 따로 안 들고다님' (2단계에서 중요!)
   - 그리고 아주 중요한 것:
       C에서 배열 이름(arr)은 사실상 '첫 번째 원소의 주소'와 같다!
       (파이썬에선 이런 개념이 없었지. 이게 나중에 함수로 배열 넘길 때 핵심이 돼)
------------------------------------------------------------

이 파일은 읽고 -> 빌드해서 -> 출력 보면서 감 잡는 용도야.
(%p 로 찍히는 주소값은 실행할 때마다 달라져. '위치'라는 것만 보면 됨)
============================================================
*/

#include <stdio.h>

int main(void) {
    // --- 1. 변수: 값을 담는 상자 ---
    int x = 5;
    printf("1) x = %d\n", x);

    // --- 2. 주소: 그 상자의 위치 ---
    // &x = "x가 메모리 어디 있는지"
    printf("2) x의 주소(&x) = %p\n", (void*)&x);

    // --- 3. 포인터: 주소를 담는 상자 ---
    int* p = &x;        // p에 'x의 주소'를 담음 (값 5가 아니라 위치!)
    printf("3) p가 담은 주소   = %p\n", (void*)p);
    printf("   p가 가리키는 값(*p) = %d\n", *p);   // *p = p가 가리키는 곳의 값 = x = 5

    // 포인터로 '원본'을 바꿀 수도 있음 (가리키는 곳을 직접 수정)
    *p = 99;            // p가 가리키는 곳(= x)에 99를 넣음
    printf("   *p = 99 한 뒤, x = %d   <- x가 바뀜! (p가 x를 가리켰으니까)\n", x);

    // --- 4. 배열: 연속된 상자들 ---
    int arr[3] = { 1, 2, 3 };
    printf("4) arr[0]=%d arr[1]=%d arr[2]=%d\n", arr[0], arr[1], arr[2]);

    // ★ 핵심: 배열 이름 arr 은 '첫 원소의 주소'와 같다
    printf("   arr     = %p   <- 배열 이름 = 첫 원소 주소\n", (void*)arr);
    printf("   &arr[0] = %p   <- 첫 원소의 주소  (위랑 같지?)\n", (void*)&arr[0]);

    // 그래서 포인터로 배열을 가리킬 수 있음
    int* q = arr;       // arr(첫 원소 주소)을 q에 담음.  q = &arr[0] 이랑 같음
    printf("   *q     = %d   <- q가 가리키는 첫 원소 (arr[0])\n", *q);
    printf("   *(q+1) = %d   <- 주소를 +1 하면 '다음 칸' (arr[1])\n", *(q + 1));

    return 0;
}

/*
[1단계 요약]
 - 변수   = 값을 담는 상자        (int x = 5;)
 - &x     = 그 상자의 주소(위치)
 - 포인터 = 주소를 담는 상자       (int* p = &x;)   *p = 가리키는 값
 - 배열   = 연속된 상자들          (int arr[3];)
 - 배열 이름 = 첫 원소의 주소!     (arr == &arr[0])  <- 2단계의 열쇠
*/
